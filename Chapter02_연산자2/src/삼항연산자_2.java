public class 삼항연산자_2 {

	public static void main(String[] args) {
		/* 변수
		 * 
		 * 
		*/
		int a;
		int b;
		a=3;
		b=a+5;
		System.out.println("1.");
		System.out.println("a의 값:"+a);
		System.out.println("b의 값:"+b);
		System.out.println("================\n");
		
		int c,d;
		c=6;
		d=2;
		System.out.println("2.");
		System.out.println("덧셈 연산 결과:"+(c+d));
		System.out.println("뺄셈 연산 결과:"+(c-d));
		System.out.println("곱셈 연산 결과:"+c*d);
		System.out.println("나누기 연산 결과:"+c/d);
		System.out.println("나머지 연산 결과:"+c%d);
		System.out.println("================\n");
		
		int num1, num2;
		num1 = 10/3;
		num2 = 10%3;
		System.out.println("3.");
		System.out.println("몫:"+num1);
		System.out.println("나머지:"+num2);
		System.out.println("================\n");
		
		
		
	}

}
